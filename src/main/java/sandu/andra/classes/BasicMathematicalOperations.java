package sandu.andra.classes;

public class BasicMathematicalOperations {

    //addition operation
    public static int additionOperation(int op1, int op2) {
        int sum = op1 + op2;
        return sum;
    }

    //subtraction operation
    public static int subtractionOperation(int op1, int op2) {
        int subtraction = op1 - op2;
        return subtraction;
    }

    //product operation
    public static int productOperation(int op1, int op2) {
        int product = op1 * op2;
        return product;
    }

    //square operation
    public static int squareOperation(int op) {
        int square = op * op;
        return square;
    }
}
